    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
        File file= new File("E:\\text.txt");
        if(!file.exists()){
            file.createNewFile();
        }
         
        FileWriter fw= new FileWriter(file.getAbsoluteFile());
        BufferedWriter bw = new BufferedWriter(fw);
        
        for(int i=0; i< jTable1.getRowCount(); i++){
            for(int j=0; j< jTable1.getColumnCount(); j++){
                bw.write(jTable1.getModel().getValueAt(i,j)+" ");
            }
            bw.write("\n        \n");
            
        }
        bw.close();
        fw.close();
        JOptionPane.showMessageDialog(null,"SAVED");
        
        }catch(Exception ex){
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jtxtRANKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtxtRANKActionPerformed
        // TODO add your handling code here:
        double[] P = new double[50];
        
        P[0]=Double.parseDouble(jtxtAMIII.getText());
        P[1]=Double.parseDouble(jtxtDS.getText());
        P[2]=Double.parseDouble(jtxtDLDA.getText());
        P[3]=Double.parseDouble(jtxtD.getText());
        P[4]=Double.parseDouble(jtxtECCF.getText());
        P[5]=Double.parseDouble(jtxtOOPM.getText());
        
        P[6]=P[0]+P[1]+P[2]+P[3]+P[4]+P[5];
        
         if (P[6]>=500)
        {
           jtxtRANK.setText("Distinction");
        }
        else if (P[0]<30 || P[1]<30 || P[2]<30 || P[3]<30 || P[4]<30 || P[5]<30)
        {
             jtxtRANK.setText("Fail");
        }    
        else if (P[6]>=400)
        {
            jtxtRANK.setText("First Class");
        }
        else if (P[6]>=300)
        {
            jtxtRANK.setText("Second Class");
        }
         else  if (P[6]>=150)
        {
            jtxtRANK.setText("Third Class");
        }
         else  if (P[6]<=150)
        {
            jtxtRANK.setText("Fail");
        }
             
    }//GEN-LAST:event_jtxtRANKActionPerformed

